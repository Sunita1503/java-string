package com.sunita.assignments.string;

//Demo java program to covert string to lower case
public class convertStringToLowercase {

public static void convertStringToLowercase(String strName){
		
	//Use of toLowerCase
		System.out.println("String converted to Uppercase : " + strName.toLowerCase());
		
	}
	
	public static void main(String[] args) {
		
		convertStringToLowercase("ComputeR");
		convertStringToLowercase("SHRIRAMPUR");
		

	}

}
